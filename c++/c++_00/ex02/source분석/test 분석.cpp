#include <vector>
#include <algorithm>
#include <functional>
#include "Account.hpp"


int		main( void ) {

	typedef std::vector<Account::t>							  accounts_t;		//account의 벡터
	typedef std::vector<int>								  ints_t;			//int형의 벡터..
	typedef std::pair<accounts_t::iterator, ints_t::iterator> acc_int_t;		//account 벡터, int형 벡터의 iterator의 pair.

	int	const				amounts[]	= { 42, 54, 957, 432, 1234, 0, 754, 16576 };
	size_t const			amounts_size( sizeof(amounts) / sizeof(int) );	//amounts의 length.
	accounts_t				accounts( amounts, amounts + amounts_size );	//initializing a vector : from array : vect(arr, arr + 개수)
	accounts_t::iterator	acc_begin	= accounts.begin();					//accounts의 iterator의 begin, end설정.
	accounts_t::iterator	acc_end		= accounts.end();

	int	const			d[]			= { 5, 765, 564, 2, 87, 23, 9, 20 };
	size_t const		d_size( sizeof(d) / sizeof(int) );
	ints_t				deposits( d, d + d_size );
	ints_t::iterator	dep_begin	= deposits.begin();
	ints_t::iterator	dep_end		= deposits.end();

	int	const			w[]			= { 321, 34, 657, 4, 76, 275, 657, 7654 };
	size_t const		w_size( sizeof(w) / sizeof(int) );
	ints_t				withdrawals( w, w + w_size );
	ints_t::iterator	wit_begin	= withdrawals.begin();
	ints_t::iterator	wit_end		= withdrawals.end();
	// accounts_t vector, deposits vecotr 생성 + iterator begin, end 생성.

	Account::displayAccountsInfos();
	std::for_each( acc_begin, acc_end, std::mem_fun_ref( &Account::displayStatus ) );

	//deposit을 넣어주는 부분.
	for ( acc_int_t it( acc_begin, dep_begin );						//iterator의 pair를 선언한 뒤에 둘 다 끝일때까지 돌아준다.
		  it.first != acc_end && it.second != dep_end;
		  ++(it.first), ++(it.second) ) {

		(*(it.first)).makeDeposit( *(it.second) );				//account.makedeposit()의 deposit을 넣어준다.
	}
				//displayAccountsinfo랑 displayStatus랑 다른듯>

	Account::displayAccountsInfos();
	std::for_each( acc_begin, acc_end, std::mem_fun_ref( &Account::displayStatus ) );

	for ( acc_int_t it( acc_begin, wit_begin );
		  it.first != acc_end && it.second != wit_end;
		  ++(it.first), ++(it.second) ) {

		(*(it.first)).makeWithdrawal( *(it.second) );
	}

	Account::displayAccountsInfos();
	std::for_each( acc_begin, acc_end, std::mem_fun_ref( &Account::displayStatus ) );

	return 0;
}


